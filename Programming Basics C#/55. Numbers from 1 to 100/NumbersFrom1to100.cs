/*
 ⦁	Числа от 1 до 100
Напишете програма, която отпечатва числата от 1 до 100, всяко на нов ред.

 */


using System;


namespace _55.Numbers_from_1_to_100
{
    internal class NumbersFrom1to100
    {
        static void Main(string[] args)
        {
            for(int i = 1; i <= 100; i++)
            {
                Console.WriteLine(i);
            }
        }
    }
}
